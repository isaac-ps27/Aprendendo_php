<?php

    echo 5 ** 2 ;// the 5 is the base and the 2 is the exponent, so it will calculate 5 to the power of 2, which is 25
    echo "<br>";
    echo 2 ** 10 ;// the 2 is the base and the 10 is the exponent, so it will calculate 2 to the power of 10, which is 1024
    echo "<br>";

    $a = 7;
    $b = 4;

    echo $a ** $b ;// the $a is the base and the $b is the exponent, so it will calculate 7 to the power of 4, which is 2401
    echo "<br>";

    $c = 12;

    $d = $c ** $a ;// the $c is the base and the $a is the exponent, so it will calculate 12 to the power of 7, which is 35831808
